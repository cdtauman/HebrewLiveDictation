def ui_language(config) -> str:
    value = config.get("app.ui_language", "he") if hasattr(config, "get") else str(config or "he")
    return "he" if str(value).lower().startswith("he") else "en"


def is_rtl(config) -> bool:
    return ui_language(config) == "he"


