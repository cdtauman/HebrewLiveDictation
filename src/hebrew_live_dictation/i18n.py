def tr(config, key: str, **kwargs) -> str:
    language = ui_language(config)
    value = TRANSLATIONS.get(language, TRANSLATIONS["en"]).get(key)
    if value is None:
        value = TRANSLATIONS["en"].get(key, key)
    return value.format(**kwargs) if kwargs else value


def friendly_error(config, message: str) -> str:
    raw = str(message or "")
    lowered = raw.lower()
    if "default credentials" in lowered or "application default credentials" in lowered:
        return tr(config, "error_adc_missing")
    if "credentials file not found" in lowered or "credentials are not configured" in lowered:
        return tr(config, "error_json_missing")
    if "microphone" in lowered or "audio stream" in lowered:
        return tr(config, "error_microphone")
    if "google" in lowered or "speech" in lowered or "grpc" in lowered:
        return tr(config, "error_google_connection")
    return f"{tr(config, 'error_prefix')} {raw}"
